CBlender_sepia::CBlender_sepia()
{
	description.CLS = 0;
}

CBlender_sepia::~CBlender_sepia()
{
}

void CBlender_sepia::Compile(CBlender_Compile& C)
{
	IBlender::Compile(C);

	switch (C.iElement)
	{
	case 0:
		C.r_Pass("null", "postprocess_stage_sepia", FALSE, FALSE, FALSE);
		C.r_Sampler_rtf("s_image", r2_RT_GBuffer_Albedo);
		jitter(C);
		C.r_End();
		break;
	}
}
