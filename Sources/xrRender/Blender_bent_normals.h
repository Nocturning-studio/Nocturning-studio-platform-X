class CBlender_bent_normals : public IBlender
{
  public:
	virtual LPCSTR getComment()
	{
		return "INTERNAL: Bent normals";
	}

	void Compile(CBlender_Compile& C)
	{
		IBlender::Compile(C);

		switch (C.iElement)
		{
		case 0:
			C.begin_Pass("screen_quad", "bent_normals_stage", FALSE, FALSE, FALSE);
			gbuffer(C);
			jitter(C);
			C.end_Pass();
			break;
		}
	}

	CBlender_bent_normals() = default;
	virtual ~CBlender_bent_normals() = default;
};
